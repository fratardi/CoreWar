#include "libft.h"

int	ft_isdigit(int c)
{
	if (c < '0' || c > '9')
		return (0);
	return (1);
}

int	ft_ishexdigit(char c)
{
	if (ft_isdigit(c) || (c >= 'A' && c <= 'F') ||
			(c >= 'a' && c <= 'f'))
		return (1);
	else
		return (0);
}
